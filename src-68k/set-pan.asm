
; ==============================================================================
; ------------------------------------------------------------------------------
; Sets panning for the normal samples
; ------------------------------------------------------------------------------

MegaPCM_SetPan:

	; ----------------------------------------------------------------------
	; INPUT:
	@pan:				equr	d0	; .b	LR-pan ($40, $80 or $C0)
	; ----------------------------------------------------------------------

	stopZ80
	move.b	@pan, Z80_RAM+Z_MPCM_PanInput
	startZ80
	rts


; ==============================================================================
; ------------------------------------------------------------------------------
; Sets panning for SFX samples (with FLAGS_SFX)
; ------------------------------------------------------------------------------

MegaPCM_SetSFXPan:

	; ----------------------------------------------------------------------
	; INPUT:
	@pan:				equr	d0	; .b	LR-pan ($40, $80 or $C0)
	; ----------------------------------------------------------------------

	stopZ80
	move.b	@pan, Z80_RAM+Z_MPCM_SFXPanInput
	startZ80
	rts
