
; ==============================================================================
; ------------------------------------------------------------------------------
; Sets volume for the normal samples
; ------------------------------------------------------------------------------

MegaPCM_SetVolume:

	; ----------------------------------------------------------------------
	; INPUT:
	@volume:			equr	d0	; .b	volume level (0 = max, $F = min)
	; ----------------------------------------------------------------------

	stopZ80
	move.b	@volume, Z80_RAM+Z_MPCM_VolumeInput
	startZ80
	rts


; ==============================================================================
; ------------------------------------------------------------------------------
; Sets volume for SFX samples (with FLAGS_SFX)
; ------------------------------------------------------------------------------

MegaPCM_SetSFXVolume:

	; ----------------------------------------------------------------------
	; INPUT:
	@volume:			equr	d0	; .b	volume level (0 = max, $F = min)
	; ----------------------------------------------------------------------

	stopZ80
	move.b	@volume, Z80_RAM+Z_MPCM_SFXVolumeInput
	startZ80
	rts
